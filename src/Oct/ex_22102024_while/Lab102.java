package Oct.ex_22102024_while;

public class Lab102 {
    public static void main(String[] args) {
        int i=0;
        while(i<10){
            System.out.println(i); //10 times
            i++;
        }
        for (int j = 0; j < 10; j++) {
            System.out.println(j);
        }
    }
}
