public class Lab038 {
    public static void main(String[] args) {
        //Arithmetic
//        + Addition
//        - Substration
//        * Multiplication
//        / Division
//        % Modulo- Remainder
        int a = 20;
        int b = 10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b); //0
    }
}
