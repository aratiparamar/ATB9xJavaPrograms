package Oct.ex_11102024_operators;

public class Lab048 {
    public static void main(String[] args) {
        // OR gate conditions
        System.out.println(true ||  true);
        System.out.println(true ||  false);
        System.out.println(false ||  true);
        System.out.println(false ||  false);

        // AND gate conditions
        System.out.println(true &&  true);
        System.out.println(true &&  false);
        System.out.println(false &&  true);
        System.out.println(false &&  false);

    }
}
